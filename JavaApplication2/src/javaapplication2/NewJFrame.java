    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        // TODO add your handling code here:
    String produto = cmbProdutos.getSelectedItem().toString();
    double valor = Double.parseDouble(txtValor.getText());
    int quantidade = Integer.parseInt(jsQuant.getValue().toString());
    
    DefaultTableModel tabela =(DefaultTableModel) tblProdutos.getModel();
    
    boolean existe = false;
    for (int i = 0; i < tblProdutos.getRowCount(); i++) {
        String sNome = tblProdutos.getValueAt(i, 0).toString();     
        if (sNome.equals(produto)) {
            existe = true;
            break;
        }}
    
    if (existe) {
        JOptionPane.showMessageDialog(this, "Esse produto já está na tabela!");
    } else {
        Object[] novaLinha = new Object[]{
            produto, valor, quantidade, valor * quantidade
        };
        tabela.addRow(novaLinha);
        calcularTotal(); // atualiza o total após adicionar
    }

    
    //reset form
    
    cmbProdutos.setSelectedIndex(0);
    txtValor.setText("5");
    jsQuant.setValue(0);
    }//GEN-LAST:event_btnAdicionarActionPerformed

    private void cmbProdutosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbProdutosActionPerformed
        // TODO add your handling code here:
        
        String valor = "";
        String produto = cmbProdutos.getSelectedItem().toString();
        
        switch (produto) {
            case "Arroz":
                valor = "5";
                break;
                
            case "Feijão":
                valor = "9";
                break;
                
            case "Batata":
                valor = "7";
                break;
                
            case "Frango":
                valor = "12";
                break;
                
        }
        txtValor.setText(valor);
    }//GEN-LAST:event_cmbProdutosActionPerformed

