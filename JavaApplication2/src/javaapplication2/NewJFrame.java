    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        // TODO add your handling code here:
        int linhaSelecionada =tblProdutos.getSelectedRow();
        if(linhaSelecionada == -1){
            JOptionPane.showMessageDialog(this, "Selecione uma linha na tabela!!");
        }
        else{
            int op = JOptionPane.showConfirmDialog(this, "Desja realmente excluir esse produto?");
        
            if(op == 0){
                DefaultTableModel tabela =(DefaultTableModel) tblProdutos.getModel();
                tabela.removeRow(linhaSelecionada);
                calcularTotal();
        }}
    }//GEN-LAST:event_btnRemoverActionPerformed
    private void calcularTotal(){
        double total=0;
        for(int i =0; i<tblProdutos.getRowCount();i++){
            String sValor =tblProdutos.getValueAt(i, 3).toString();
            double valor = Double.parseDouble(sValor);
            total += valor;
    }
    txtValorTotal.setText (String.valueOf(total));
    }
    
    private void valorDuplicado(){
         for(int i =0; i<tblProdutos.getRowCount();i++){
            String sNome =tblProdutos.getValueAt(i, 1).toString();
            if(sNome == cmbProdutos.getSelectedItem().toString()){
                JOptionPane.showMessageDialog(this, "Esse produto ja esta na tabela!!");
                
            }}
    }
